class Solution {
  public:
    // Function to split a linked list into two lists alternately
    vector<Node*> alternatingSplitList(struct Node* head) {
       if(!head->next)return {head,NULL};
        Node* a=new Node(head->data);
        Node* ahead=a;
        Node* b=new Node(head->next->data);
        Node*bhead=b;
        head=head->next->next;
        while(head and head->next){
            a->next=head;
            a=a->next;
            b->next=head->next;
            b=b->next;
            head=head->next->next;
        }
        if(head)a->next=head;
        else a->next=NULL;
        b->next=NULL;
        return {ahead,bhead};
    }
};


